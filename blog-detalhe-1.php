                <p>O projeto visa facilitar a incorporação do funcionário, seja ele novo, ou de casa, dentro de todas as etapas da empresa, através de um simulador virtual 3d, aonde este possa visualizar todos os setores da empresa, realizar seus procedimentos, participar de quiz e testes de compreensão. </p>

                <p>As atuais ferramentas encontradas no mercado são voltadas para dinâmicas em grupos, para se ter uma noção da atual capacidade do funcionário dentro de sua função. A ferramenta proposta insere o usuário dentro de um ambiente empresarial virtual, aonde ele vai ter oportunidade de aprender todos os procedimentos da empresa dentro de sua função ou da nova função aonde será enquadrado, será uma ótima ferramenta para as empresas em dias de integração ou mesmo em momentos de reestruturação empresarial com mudança de organograma, processos, funções e outros. </p>

