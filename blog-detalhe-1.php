                  <p class="post-classic-text">A área de Recursos Humanos das empresas depara-se com um fenômeno chamado turnover ou rotatividade de pessoal.</p> 

                  <p class="post-classic-text">Os desligamentos de funcionários das empresas e sua reposição geram alguns impactos nas organizações, como custos, que podem ser financeiros ou econômicos (de difícil mensuração) e não reconhecidos contabilmente. </p>

                  <p class="post-classic-text">Existe a necessidade de se conhecer os motivos que levam as pessoas a se desligarem de uma organização, para que as mesmas possam tomar algumas medidas preventivas para a retenção de talentos e conhecimeento, o que gera uma redução de custos. </p>

                  <p class="post-classic-text">Para a empresa ser desenvolvida, não é suficiente ela produzir, ter recursos financeiros ou tecnologia avançada, mas ter pessoas qualificadas e comprometidas com o trabalho, buscando o desenvolvimento da empresa. Segundo Chiavenato (2005, p. 5), “em resumo, as organizações são constituídas de pessoas e dependem delas para atingir seu objetivo e cumprir sua missão”. O trabalhador, que até pouco tempo atrás conservava seu emprego por anos, hoje tem outra visão: está em busca de novos desafios, está buscando conhecer mais sobre a empresa onde atua, seus objetivos e o seu futuro dentro dela. Os funcionários passaram a ser mais exigentes, a valorizar os benefícios e a qualidade de vida no trabalho. </p>

                  <p class="post-classic-text">As mudanças de gerações de Baby Boomers, X, até geração Y nas empresas mudaram o perfil e as exigências dos profissionais dentro das mesmas. </p>

                  <p class="post-classic-text">O tema turnover é de grande importância no processo organizacional, pois este processo envolve pessoas, custos e a organização como um todo. O turnover gera descontentamento dos demais colaboradores, queda na produtividade, perda de informações, dentre outros. </p>

                  <p class="post-classic-text">As pessoas fazem parte da organização e executam os processos. Sem elas, a organização não tem sentido. Reconhecer o potencial que faz parte da empresa e fazer com que o mesmo se dedique a ela por muito tempo é um processo de organização e empenho da mesma. </p>

                  <p class="post-classic-text">Buscando otimizar a área de treinamento das empresas e a retenção de conhecimento o Projeto busca de uma maneira inovativa e lúdica de minimizar o turnover e reter os talentos nas empresas. </p>

